package edu.uam.educore.enums;

/**
 * Categorías de espacios físicos disponibles en los centros educativos.
 * Determina el propósito técnico e inmobiliario del aula.
 */
public enum TipoAula {
  /** Espacio estándar para lecciones magistrales habituales. */
  REGULAR,
  
  /** Espacio equipado con recursos tecnológicos o computacionales especiales. */
  LABORATORIO,
  
  /** Espacio de alta capacidad adaptado para ponencias, charlas o conferencias. */
  AUDITORIO
}